package praktikum_1.latihan;

public class GenapGanjil {
    public static void main(String[] args) {
        int angka = 7;

        if (angka % 2 == 0) {
            System.out.println("Angka " + angka + " adalah bilangan genap.");
        } else {
            System.out.println("Angka " + angka + " adalah bilangan ganjil.");
        }
    }
}